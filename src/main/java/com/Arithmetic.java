package com;


public class Arithmetic {
    public double add(double a, double b) {
        return a + b;
    }

    public double minus(double a, double b) {

        return a - b ;
    }

    public double mult(double a, double b) {

        return a * b;
    }

    public double div(double a, double b) {
        return a / b;
    }
}
